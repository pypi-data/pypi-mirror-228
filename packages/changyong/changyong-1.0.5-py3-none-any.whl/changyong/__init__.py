# 读取指定路径下所有文件
def get_file_du(path):
    lujing = []
    filelist = os.listdir(path)
    for filename in filelist:
        filepath = os.path.join(path, filename)
        if os.path.isdir(filepath):
            get_file_du(filepath)
        lujing.append(filepath)
    return lujing


