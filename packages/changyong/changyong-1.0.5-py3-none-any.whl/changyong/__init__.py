# 邮箱提醒（含附件）
def email_fujian(smtp, key, user, user_to, title, content, fujian):
    # 定义发件人邮箱
    user = user
    # QQ邮箱POP3/SMTP的授权码
    pwd = key
    # 自定义收件人
    to = user_to
    # 创建一个可以同时添加正文和附件的msg
    msg = MIMEMultipart()
    # 设置HTML格式的邮件正文
    # 三个单引号表示包围注释内容，用来包含HTML代码
    mail_msg = content
    # 如果仅添加正文可以用此公式：msg=MIMEText(mail_msg,'html','utf-8')#表示添加正文内容
    msg.attach(MIMEText(mail_msg, 'html', 'utf-8'))  # 添加正文
    # 添加附件
    att1 = MIMEText(open(fujian, 'rb').read(), 'base64', 'utf-8')  # 添加附件，由于定义了中文编码，所以文件可以带中文
    att1["Content-Type"] = 'application/octet-stream'  # 数据传输类型的定义
    att1["Content-Disposition"] = 'attachment;filename=%s' % fujian  # 定义文件在邮件中显示的文件名和后缀名，名字不可为中文
    msg.attach(att1)  # 将附件添加到邮件内容当中
    # 配置调用邮件信息
    msg['Subject'] = title  # 设置邮件主题
    msg['From'] = user  # 设置发件人
    msg['To'] = to  # 设置收件人
    # 执行命令
    s = smtplib.SMTP_SSL(smtp,
                         465)  # 选择QQ邮箱服务，默认端口465（smtplib.SMTP()：构造函数，功能是与smtp服务器建立连接，连接成功后，就可以向服务器发送相关请求，比如登录，校验，发送，退出）
    s.login(user, pwd)  # 登录QQ邮箱
    s.send_message(msg)  # 发送邮件
    s.quit()  # 退出QQ邮箱服务
    print('成功发送')


# 报错邮箱(不含附件)
def email(smtp, key, user, user_to, title, content):
    # 构造邮件内容
    msg = MIMEText(content)
    # 设置邮件主题
    msg["Subject"] = title
    # 发送者
    msg['From'] = user
    # 邮箱服务账号
    from_addr = user
    # 邮箱服务器密码
    password = key
    # 收件人地址
    to_addr = user_to
    # smtp服务器地址
    smtp_server = smtp
    # smtp协议的默认端口是25，QQ邮箱smtp服务器端口是465，第三个参数是超时设置
    server = smtplib.SMTP_SSL(smtp_server, 465, timeout=2)
    server.ehlo()
    # 登录邮箱
    server.login(from_addr, password)
    # 发送邮件，第一个参数是发送方地址，第二个参数是接收方列表，列表中可以有多个接收方地址，msg.as_string()将MIMEText对象转化成文本
    server.sendmail(from_addr, [to_addr], msg.as_string())
    # 关闭邮箱
    server.quit()
    print('成功发送邮件')


